import type { AtlasEffect, AtlasTask } from "./field-types";

const retryFollowup = (title: string): AtlasEffect => ({
  type: "create_followup_task",
  daysAfter: 1,
  title,
  actionType: "direct_sow",
});

const germinationFollowup = (objectId: string, crop: string): AtlasEffect => ({
  type: "create_followup_task",
  daysAfter: 5,
  title: `Check ${objectId} for ${crop} germination, washout, or bird disturbance`,
  actionType: "field_check",
});

const waterFollowup = (objectId: string, crop: string): AtlasEffect => ({
  type: "create_followup_task",
  daysAfter: 1,
  title: `Water check: ${objectId} ${crop}`,
  actionType: "water_check",
});

const seededEffects = (objectId: string, crop: string): AtlasEffect[] => [
  {
    type: "set_object_state",
    objectId,
    nextState: "seeded",
  },
  {
    type: "start_timer",
    objectId,
    timerName: "germination",
    days: 5,
  },
  waterFollowup(objectId, crop),
  germinationFollowup(objectId, crop),
];

const fieldRowSowTask = ({
  date,
  bedNumber,
  crop,
  packet,
  unlockText,
}: {
  date: string;
  bedNumber: number;
  crop: string;
  packet: string;
  unlockText: string;
}): AtlasTask => {
  const objectId = `field_row_${bedNumber}`;

  return {
    id: `2026-${date}-field-row-${bedNumber}`,
    date: `2026-${date}`,
    title: `Earthway sow Field Row ${bedNumber}`,
    areaId: "field_rows",
    objectId,
    actionType: "direct_sow",
    packet,
    also: "Sow horse pasture grass in the walkway if that walkway is ready.",
    instructions: `Earthway sow Field Row ${bedNumber}. Packet: ${packet}. Sow horse pasture grass in the walkway if that walkway is ready. Keep this field simple and productive; do not redesign while sowing.`,
    unlockText,
    status: "open",
    ifDone: seededEffects(objectId, crop),
    ifSkipped: [retryFollowup(`Try again: Earthway sow Field Row ${bedNumber}`)],
  };
};

